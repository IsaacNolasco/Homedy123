package com.example.homedy;

public class CategoriasDTO {

    private String id;
    private String dolores;
    private String golpes;
    private String cuidado_personal;
    private String mascotas;
    private String bajar_de_peso;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDolores() {
        return dolores;
    }

    public void setDolores(String dolores) {
        this.dolores = dolores;
    }

    public String getGolpes() {
        return golpes;
    }

    public void setGolpes(String golpes) {
        this.golpes = golpes;
    }

    public String getCuidado_personal() {
        return cuidado_personal;
    }

    public void setCuidado_personal(String cuidado_personal) {
        this.cuidado_personal = cuidado_personal;
    }

    public String getMascotas() {
        return mascotas;
    }

    public void setMascotas(String mascotas) {
        this.mascotas = mascotas;
    }

    public String getBajar_de_peso() {
        return bajar_de_peso;
    }

    public void setBajar_de_peso(String bajar_de_peso) {
        this.bajar_de_peso = bajar_de_peso;
    }
}
